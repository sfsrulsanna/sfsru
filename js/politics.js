// Показать информацию о ветви власти
function showBranchInfo(branchType) {
    const branch = branchData[branchType];
    if (!branch) return;

    const modalContent = document.getElementById('modalContent');
    modalContent.innerHTML = `
        <div class="modal-branch-info">
            <h3>${branch.icon} ${branch.title}</h3>
            <div>${branch.description}</div>
        </div>
    `;

    const modal = document.getElementById('branchModal');
    modal.style.display = 'block';

    // Добавляем обработчик для закрытия по клику вне модального окна
    modal.addEventListener('click', function(event) {
        if (event.target === modal) {
            closeModal();
        }
    });

    // Добавляем обработчик для закрытия по ESC
    document.addEventListener('keydown', function(event) {
        if (event.key === 'Escape') {
            closeModal();
        }
    });
}

// Закрыть модальное окно
function closeModal() {
    const modal = document.getElementById('branchModal');
    modal.style.display = 'none';
}

// Анимация появления карточек при загрузке страницы
document.addEventListener('DOMContentLoaded', function() {
    // Анимация для карточек ветвей власти
    const branchCards = document.querySelectorAll('.branch-card');
    branchCards.forEach((card, index) => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(30px)';
        
        setTimeout(() => {
            card.style.transition = 'all 0.6s ease';
            card.style.opacity = '1';
            card.style.transform = 'translateY(0)';
        }, 200 * index);
    });

    // Добавляем интерактивность карточкам
    branchCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-8px) scale(1.02)';
        });

        card.addEventListener('mouseleave', function() {
            this.style.transform = 'translateY(0) scale(1)';
        });
    });

    console.log('Страница политики СФСРЮ загружена');
});